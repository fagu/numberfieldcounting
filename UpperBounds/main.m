// Let G be a permutation group of degree >= 2.
// Returns a list of discriminant multiplicity bounds for G.
function ComputeDMBs(G)
	res := [];
	n := Degree(G);
	assert n ge 2;
	
	// Parameterizations in degrees <= 5 (Theorem 3.9).
	if n le 5 then
		Append(~res, rec<DMB |
			bound := 1,
			proof := "low-degree"
		>);
	end if;
	// TODO Other known special cases?
	// Schmidt bound (Theorem 3.13).
	Append(~res, rec<DMB |
		bound := (n+2)/4,
		proof := "schmidt"
	>);
	// Bhargava-Shankar-Wang (Theorem 3.14).
	if n ge 6 then
		g := Floor((n-1)/2);
		delta := 1/(2*n-2) - 1/(2^(2*g) * (2*n-2));
		Append(~res, rec<DMB |
			bound := (n+2)/4 - delta,
			proof := "bhargava-shankar-wang"
		>);
	end if;
	// Bhargava-Schmidt bound (Theorem 3.15).
	if IsPrimitive(G) then
		indG := Min([n - #CycleDecomposition(g[3]) : g in Classes(G) | not IsIdentity(g[3])]);
		Append(~res, rec<DMB |
			bound := (n-2)/4 + 1/indG,
			proof := "bhargava-schmidt"
		>);
	end if;
	// TODO Schmidt bound for towers (Theorem 3.16, Problem 3.17).
	// TODO Bounds from other invariants (Theorem 3.19, ...).
	
	// Return only the smallest bound.
	if #res ge 1 then
		best := res[1];
		for dmb in res do
			if best`bound gt dmb`bound then
				best := dmb;
			end if;
		end for;
		return [best];
	else
		return [];
	end if;
end function;

// Returns the comparison function x, y |-> f(x) < f(y).
function ByKey(f)
	return func<x, y | f(x) lt f(y) select -1 else (f(x) eq f(y) select 0 else 1)>;
end function;

// Returns a pruned list of imbs.
// 1) If any imb vector appears several times in the input, only the first one is kept. We thus only preserve the first proof trace for this vector.
// 2) Afterwards, any imb v is removed if there is another imb w with w <= v coordinate-wise.
// The running time is O(n^2 m) if there are n imbs in the input, which lie in m-dimensional space.
function PrunePartialOrder(imbs)
	if #imbs eq 0 then
		return [];
	end if;
	m := #imbs[1]`bound;
	// Order the imbs lexicographically.
	Sort(~imbs, ByKey(func<x | x`bound>));
	res := [];
	for v in imbs do
		// Only add v to res if there is no w that has already been added and that satisfies w <= v coordinate-wise.
		if not &or[&and[w`bound[i] le v`bound[i] : i in [1..m]] : w in res] then
			Append(~res, v);
		end if;
	end for;
	return res;
end function;

// Only keeps one imb per vertex of the convex hull of the input imbs + the positive octant.
function PruneConvexHull(imbs)
	point_to_imb := AssociativeArray();
	for imb in imbs do
		point_to_imb[imb`bound] := imb;
	end for;
	
	remaining_points := ConvexHullShadowGenerators([imb`bound : imb in imbs]);
	return [point_to_imb[v] : v in remaining_points];
	
