Qrat := Rationals();

// A group together with an ordering of the conjugacy classes and inertia types.
GroupCoordinates := recformat<
	// The finite group.
	group : Grp,
	// A list of representatives of the conjugacy classes of elements.
	classes : SeqEnum,
	// A list of elements generating representatives of the inertia types.
	// (An inertia type is a conjugacy class of nontrivial cyclic subgroups.)
	inertia_types : SeqEnum,
	// conj_cl_to_inertia_type[i] = the index of the inertia type containing the cyclic group generated by classes[i]. (By convention, this is 0 if classes[i] = identity.)
	conj_cl_to_inertia_type : SeqEnum
>;

GroupData := recformat<
	// The GroupCoordinates.
	coords,
	// The inertial multiplicity bounds.
	imbs : SeqEnum
>;

// Inertial multiplicity bound.
IMB := recformat<
	bound : SeqEnum,
	proof
>;

// Discriminant multiplicity bound.
DMB := recformat<
	bound : Qrat,
	proof
>;

// Class group bound.
// TODO Document what exactly this means. Also, sum_savings is a horrible name.
CGB := recformat<
	sum_savings : Qrat,
	bound : Qrat,
	proof
>;

NormSubDat := recformat<
	subgroup,
	bounds
>;

// Returns the index of the inertial type <g>. Returns 0 if g is the identity.
function InertiaTypeGeneratedBy(coords, g)
	// We find the conjugacy class containing g by trying one conjugacy class at a time.
	// TODO There may be faster ways to do this.
	for i in [1..#coords`classes] do
		if IsConjugate(coords`group, coords`classes[i], g) then
			return coords`conj_cl_to_inertia_type[i];
		end if;
	end for;
	error "Did not find the conjugacy class containing g.";
end function;

// Computes GroupCoordinates for this group.
function ComputeGroupCoordinates(G)
	inertia_types := [];
	classes := Classes(G);
	power_map := PowerMap(G);
	n := #classes;
	conj_cl_to_inertia_type := [0 : i in [1..n]];
	for i in [1..n] do
		if conj_cl_to_inertia_type[i] eq 0 then
			// Generator of the conjugacy class.
			g := classes[i][3];
			// Order of g.
			ord := classes[i][1];
			if ord ne 1 then
				Append(~inertia_types, g);
				// Loop over all generators of the cyclic group generated by g.
				for j in [1..ord] do
					if Gcd(j, ord) eq 1 then
						conj_cl_to_inertia_type[power_map(i, j)] := #inertia_types;
					end if;
				end for;
			end if;
		end if;
	end for;
	return rec<GroupCoordinates |
		group := G,
		classes := [cl[3] : cl in classes],
		inertia_types := inertia_types,
		conj_cl_to_inertia_type := conj_cl_to_inertia_type
	>;
end function;

